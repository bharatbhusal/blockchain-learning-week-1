// Exercise 3: Functions
// Task:
//   - Write a JavaScript function that takes two numbers as parameters and returns their sum.
//   - Test the function with different inputs and log the results to the console.

function sum(num1, num2) {
    return num1 + num2
}

console.log(`Sum of 1, 2: ${sum(1, 2)}`)
console.log(`Sum of 1, 20: ${sum(1, 20)}`)
console.log(`Sum of 11, 24: ${sum(11, 24)}`)
console.log(`Sum of -1, 2: ${sum(-1, 2)}`)
console.log(`Sum of 31, 2: ${sum(31, 2)}`)